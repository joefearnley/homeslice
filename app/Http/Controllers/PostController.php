     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'body' => 'required'
        ]);

        $post = new Post();
        $post->body = $request->input('body');
        $post->user_id = Auth::id();
        $post->save();

        return redirect()->route('home');
    }

